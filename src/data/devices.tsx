function sortDevices() {
  let result = Devices;
  // Sort by site name
  result = sortBy(result, (device) => device.name.toLowerCase());
  // Sort by favourite tag, favourites first
  result = sortBy(result, (device) => !device.tags.includes('favourite'));
  return result;
}

